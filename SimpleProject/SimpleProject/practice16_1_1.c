#include<stdio.h>
//2차원 배열을 선언하여 구구단준 2단,3단,4단을 저장하고 출력하는 예제
int main(void) {
	int gugu[3][9]={{2,4,6,8,10,12,14,16,18},{3,6,9,12,15,18,21,24,27}
	,{4,8,12,16,20,24,28,32,36} };
	int i = 0, j = 0;
	for (i = 0; i < 3; i++) {
		for (j = 0; j < 9; j++) {
			printf("%d ", gugu[i][j]);
		}
		printf("\n");
	}
	return 0;
}